export function getInterpretation(
  type: string,
  companyName: string
): Interpretation {
  const template = templates[type] ?? templates["균형형"];

  return {
    summary: template.summary.replace("{company}", companyName),
    cards: template.cards.map((card) => ({
      title: card.title,
      description: card.description.replace("{company}", companyName),
    })),
  };
}
